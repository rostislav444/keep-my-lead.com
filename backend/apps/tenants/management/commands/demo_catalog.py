class Command(BaseCommand):
    help = 'Create demo catalog: cosmetology courses for Demo Company'

    def handle(self, *args, **options):
        try:
            tenant = Tenant.objects.get(slug='demo')
        except Tenant.DoesNotExist:
            self.stdout.write(self.style.ERROR(
                'Tenant "demo" not found. Run initial_data first.'
            ))
            return

        created_cats = 0
        created_items = 0

        for order, cat_data in enumerate(CATEGORIES):
            category, cat_created = Category.objects.get_or_create(
                tenant=tenant,
                name=cat_data['name'],
                defaults={
                    'description': cat_data['description'],
                    'order': order,
                },
            )
            if cat_created:
                created_cats += 1

            for item_data in cat_data['items']:
                _, item_created = Item.objects.get_or_create(
                    tenant=tenant,
                    name=item_data['name'],
                    defaults={
                        'category': category,
                        'context': item_data['context'],
                    },
                )
                if item_created:
                    created_items += 1

        self.stdout.write(self.style.SUCCESS(
            f'Done! Created {created_cats} categories, {created_items} items'
        ))
